# Day 3 - Data Types Practice
name = "Ali"
age = 21
height = 5.8
is_student = True

# Print output with explanation
print(type(name), name)  # <class 'str'> Ali
print(type(age), age, "years old")  # <class 'int'> 21 years old
print(type(height), "height is", height, "inch")
print(type(is_student), "Student?", is_student)
